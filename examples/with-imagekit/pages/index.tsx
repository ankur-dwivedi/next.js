import Head from "next/head";
import { useEffect, useState } from "react";
import { listFiles } from "@/utils/imagekit";
import Masonry from "react-responsive-masonry";
import styles from "@/styles/Home.module.css";
import { FilterType } from "@/utils/types";
import { FilterEnum } from "@/utils/enum";
import { FileObject } from "imagekit/dist/libs/interfaces";
import { IKImage, IKVideo } from "imagekit-next";

export default function Home({
  allFiles = [],
  images = [],
  videos = [],
}: {
  allFiles: FileObject[];
  images: FileObject[];
  videos: FileObject[];
}) {
  const [filterState, setFileterState] = useState<FilterType>(FilterEnum.ALL);
  const [files, setFiles] = useState<FileObject[]>(allFiles);

  useEffect(() => {
    if (filterState === FilterEnum.ALL) setFiles(allFiles);
    else if (filterState === FilterEnum.PHOTOS) setFiles(images);
    else if (filterState === FilterEnum.VIDEOS) setFiles(videos);
  }, [filterState, allFiles, images, videos]);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className={styles.container}>
        <div className={styles.buttonContainer}>
          <button
            className={styles.button}
            onClick={() => setFileterState(FilterEnum.ALL)}
            style={
              filterState === FilterEnum.ALL
                ? { color: "#000" }
                : { color: "#a0a0a0" }
            }
          >
            All
          </button>
          <hr className={styles.hr} />
          <button
            className={styles.button}
            onClick={() => setFileterState(FilterEnum.PHOTOS)}
            style={
              filterState === FilterEnum.PHOTOS
                ? { color: "#000" }
                : { color: "#a0a0a0" }
            }
          >
            Photos
          </button>
          <hr className={styles.hr} />
          <button
            className={styles.button}
            onClick={() => setFileterState(FilterEnum.VIDEOS)}
            style={
              filterState === FilterEnum.VIDEOS
                ? { color: "#000" }
                : { color: "#a0a0a0" }
            }
          >
            Videos
          </button>
        </div>
        {files.length ? (
          <Masonry columnsCount={3} gutter="10px">
            {files.map((data, i) =>
              data.fileType === "image" ? (
                <IKImage
                  urlEndpoint={process.env.NEXT_PUBLIC_URL_ENDPOINT}
                  src={data.url}
                  width={400}
                  height={(400 * data.height) / data.width}
                  alt="test"
                  loading="lazy"
                />
              ) : (
                <IKVideo
                  key={i}
                  urlEndpoint={process.env.NEXT_PUBLIC_URL_ENDPOINT}
                  src={data.url}
                  controls
                />
              ),
            )}
          </Masonry>
        ) : (
          <></>
        )}
      </div>
    </>
  );
}

export async function getStaticProps() {
  const allFiles = await listFiles(100, 0, FilterEnum.ALL);
  const images = await listFiles(100, 0, FilterEnum.PHOTOS);
  const videos = await listFiles(100, 0, FilterEnum.VIDEOS);
  return { props: { allFiles, images, videos } };
}
